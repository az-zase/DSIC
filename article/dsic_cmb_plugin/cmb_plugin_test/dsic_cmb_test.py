"""
================================================================================
DSIC CMB-плагин — отдельный воспроизводимый тест (один файл).
Проверка параметра отлипания μ_d (ранняя Вселенная, расстояние до реликта).

Автор модели: Azamat Zaseev
Репозиторий:   https://github.com/az-zase/DSIC
================================================================================

СТАТУС ПРОВЕРЯЕМОГО ОБЪЕКТА.
  μ_d — феноменологическая надстройка над ядром DSIC (см. dsic-cmb-plugin).
  Это НЕ часть ядра: параметр зафиксирован по ОДНОМУ наблюдению (расстояние
  до поверхности последнего рассеяния, Planck) и не имеет второго независимого
  подтверждения. Данный тест НЕ доказывает μ_d — он проверяет:
    (а) что арифметика плагина самосогласована и воспроизводима;
    (б) что опорная цифра Planck верна;
    (в) что поздняя Вселенная (z ≤ z_detach) поправкой НЕ затронута;
    (г) честно показывает вырождение «амплитуда ↔ порог» (ноль ст. свободы);
    (д) очерчивает, где плагин мог бы быть проверен — и почему пока не может.

ЧТО ДЕЛАЕТ (всё в одном прогоне, без внешних файлов):
  [0] Самопроверка опорных данных Planck (вшиты, сверка с публикацией).
  [1] Геометрия ядра до реликта: μ_cmb, базовое D_M, величина недобора.
  [2] Фиксация порога μ_d и z_detach из требования D_M = d_LSS(Planck).
  [3] Контроль: поправка строго равна нулю в поздней Вселенной (z ≤ z_detach).
  [4] Степени свободы: вырождение амплитуды A и порога μ_d (честный разбор).
  [5] Где искать второй тест: след μ_d на высоком z и досягаемость зондов.

ДАННЫЕ (вшиты, сверены с первоисточниками):
  • Расстояние до последнего рассеяния: d_LSS = 13872.8 ± 25 Мпк, z* = 1089.92,
    r* = 144.43 Мпк (Planck 2018, TT,TE,EE+lowE+lensing; см., напр., разбор в
    arXiv:2311.04759, §3.6, где d_LSS,Planck = 13872.8 ± 25 Мпк).
  • Параметры ядра DSIC: μ₀ = 0.76, H₀ = 67.4 (ранняя шкала; те же, что в BAO,
    хронометрах и возрасте в основном тесте).

ЗАВИСИМОСТИ: numpy, scipy. ЗАПУСК: `python dsic-cmb-test.py` или ячейкой Colab.
================================================================================
"""

import numpy as np
from scipy.optimize import brentq

c = 299792.458  # км/с

# ==============================================================================
#  ПАРАМЕТРЫ ЯДРА И ОПОРНЫЕ ДАННЫЕ
# ==============================================================================
MU0 = 0.76      # фаза «сегодня» (ядро DSIC)
H0  = 67.4      # ранняя шкала Хаббла, км/с/Мпк

# --- Planck 2018 distance to last scattering (TT,TE,EE+lowE+lensing).
#     Вшито как опорное; сверяется в блоке [0].
PLANCK = dict(
    z_star=1089.92,        # красное смещение последнего рассеяния
    r_star=144.43,         # звуковой горизонт на z*, Мпк
    d_lss=13872.8,         # comoving distance to LSS = r*/θ*, Мпк
    d_lss_err=25.0,        # 1σ, Мпк
    theta_star=1.04110e-2, # 100θ* = 1.04110  ->  θ* в радианах = 1.04110e-2
)


# ==============================================================================
#  ГЕОМЕТРИЯ ЯДРА DSIC
# ==============================================================================
def b_dsic(m):
    """Наблюдаемый масштабный фактор b(μ) = √(1−μ²)/μ²."""
    return np.sqrt(1.0 - m * m) / (m * m)


def mu_e_of_z(zv):
    """Инверсия 1+z = b(μ₀)/b(μ_e) -> μ_e на физической ветви приёма."""
    return brentq(lambda m: b_dsic(m) - b_dsic(MU0) / (1.0 + zv),
                  MU0 + 1e-15, 1.0 - 1e-15)


def ck_const():
    """Перевод геометрии в Мпк: c/k = (c/H₀)·(2−μ₀²)/(1−μ₀²)."""
    return (c / H0) * (2.0 - MU0 ** 2) / (1.0 - MU0 ** 2)


def DM_base(mu_e):
    """Базовое сопутствующее расстояние DSIC (без поправки μ_d)."""
    ck = ck_const()
    return ck * (np.sqrt(1.0 - MU0 ** 2) / MU0 ** 2) * \
        (np.sqrt(1.0 - MU0 ** 2) - np.sqrt(1.0 - mu_e ** 2))


def DM_corrected(mu_e, mu_d):
    """Исправленное расстояние: добор arcsin-поправки выше порога μ_d.

    D_M_испр = D_M + (c/k)·[arcsin(μ_e) − arcsin(μ_d)]   при μ_e > μ_d,
    D_M_испр = D_M                                         при μ_e ≤ μ_d.
    Поправка(μ) = 1/√(1−μ²), отсечённая порогом; ∫ = arcsin μ.
    """
    base = DM_base(mu_e)
    if mu_e > mu_d:
        return base + ck_const() * (np.arcsin(mu_e) - np.arcsin(mu_d))
    return base


# ==============================================================================
#  [0] САМОПРОВЕРКА ОПОРНЫХ ДАННЫХ PLANCK
# ==============================================================================
def self_check_planck():
    print("=" * 70)
    print("[0] Самопроверка опорных данных Planck 2018 (вшиты)")
    print("=" * 70)
    # внутренняя согласованность: d_LSS должно равняться r*/θ*
    d_from_geom = PLANCK["r_star"] / PLANCK["theta_star"]
    rel = abs(d_from_geom - PLANCK["d_lss"]) / PLANCK["d_lss"]
    print(f"  z* = {PLANCK['z_star']},  r* = {PLANCK['r_star']} Мпк,  "
          f"100θ* = {100*PLANCK['theta_star']:.5f}")
    print(f"  d_LSS (вшито)   = {PLANCK['d_lss']} ± {PLANCK['d_lss_err']} Мпк")
    print(f"  d_LSS = r*/θ*   = {d_from_geom:.1f} Мпк  (расхожд. {100*rel:.2f}%)")
    if rel < 0.01:
        print("  OK — опорная цифра согласована с r* и θ* (Planck 2018).")
    else:
        raise SystemExit("СТОП: d_LSS не согласуется с r*/θ*. Проверь PLANCK.")
    print()


# ==============================================================================
#  [1] ГЕОМЕТРИЯ ЯДРА ДО РЕЛИКТА
# ==============================================================================
def step_core_geometry():
    print("=" * 70)
    print("[1] Геометрия ядра DSIC до реликта (без поправки)")
    print("=" * 70)
    ck = ck_const()
    mu_cmb = mu_e_of_z(PLANCK["z_star"])
    DM0 = DM_base(mu_cmb)
    deficit = PLANCK["d_lss"] - DM0
    print(f"  c/k                = {ck:.1f} Мпк")
    print(f"  μ_cmb(z={PLANCK['z_star']}) = {mu_cmb:.10f}")
    print(f"  D_M база до CMB    = {DM0:.1f} Мпк")
    print(f"  d_LSS Planck       = {PLANCK['d_lss']:.1f} Мпк")
    print(f"  недобор            = {deficit:.1f} Мпк  ({100*deficit/PLANCK['d_lss']:.1f} %)")
    print("  -> ядро недомеряет дальний конец; поправка нужна только там.\n")
    return mu_cmb, DM0, deficit


# ==============================================================================
#  [2] ФИКСАЦИЯ ПОРОГА μ_d
# ==============================================================================
def step_fix_threshold(mu_cmb, DM0):
    print("=" * 70)
    print("[2] Фиксация порога μ_d (естественная амплитуда A = 1)")
    print("=" * 70)
    ck = ck_const()
    target = PLANCK["d_lss"] - DM0
    # (c/k)·[arcsin(μ_cmb) − arcsin(μ_d)] = target
    mu_d = brentq(lambda md: ck * (np.arcsin(mu_cmb) - np.arcsin(md)) - target,
                  0.5, mu_cmb - 1e-12)
    z_det = b_dsic(MU0) / b_dsic(mu_d) - 1.0
    DM_corr = DM_corrected(mu_cmb, mu_d)
    print(f"  μ_d        = {mu_d:.6f}")
    print(f"  z_detach   = {z_det:.4f}")
    print(f"  D_M испр   = {DM_corr:.1f} Мпк  (цель Planck {PLANCK['d_lss']:.1f})")
    ok = abs(DM_corr - PLANCK["d_lss"]) < 1.0
    print(f"  замыкание до Planck: {'OK' if ok else 'РАСХОЖДЕНИЕ'}\n")
    if not ok:
        raise SystemExit("СТОП: исправленное D_M не совпало с d_LSS.")
    return mu_d, z_det


# ==============================================================================
#  [3] КОНТРОЛЬ: ПОЗДНЯЯ ВСЕЛЕННАЯ НЕ ЗАТРОНУТА
# ==============================================================================
def step_late_universe_intact(mu_d, z_det):
    print("=" * 70)
    print("[3] Контроль: поправка строго равна нулю при z ≤ z_detach")
    print("=" * 70)
    print(f"  {'z':>6} {'D_M база':>10} {'поправка':>10}")
    print("  " + "-" * 28)
    max_corr_below = 0.0
    for z in [0.5, 1.0, 2.33, 3.0, 4.0, z_det, z_det + 0.1, 5.0, 10.0]:
        me = mu_e_of_z(z)
        base = DM_base(me)
        corr = DM_corrected(me, mu_d) - base
        tag = "" if z <= z_det + 1e-9 else "  <- включена"
        if z <= z_det + 1e-9:
            max_corr_below = max(max_corr_below, abs(corr))
        print(f"  {z:>6.2f} {base:>10.1f} {corr:>+10.1f}{tag}")
    print(f"\n  макс. |поправка| при z ≤ z_detach = {max_corr_below:.2f} Мпк "
          f"(≈0; численный порог)")
    print("  -> все 6 бинов DESI (z ≤ 2.33) поправки НЕ чувствуют. Поздняя")
    print("     Вселенная и результаты основного теста сохранены полностью.\n")


# ==============================================================================
#  [4] СТЕПЕНИ СВОБОДЫ: ВЫРОЖДЕНИЕ АМПЛИТУДА ↔ ПОРОГ
# ==============================================================================
def step_degeneracy(mu_cmb, DM0):
    print("=" * 70)
    print("[4] Степени свободы: вырождение амплитуды A и порога μ_d")
    print("=" * 70)
    ck = ck_const()
    target = PLANCK["d_lss"] - DM0
    print("  Уравнение A·(c/k)·[arcsin(μ_cmb) − arcsin(μ_d)] = недобор имеет")
    print("  КРИВУЮ решений (A, μ_d): любая амплитуда даёт своё μ_d с тем же")
    print("  расстоянием до CMB. Выбор A = 1 фиксирует μ_d, но это ВЫБОР формы,")
    print("  а не вывод из данных.\n")
    print(f"  {'A':>6} {'μ_d':>10} {'z_detach':>10}  (все дают d_LSS=Planck)")
    print("  " + "-" * 36)
    for A in [0.5, 0.8, 1.0, 1.2, 2.0, 5.0]:
        arg = np.arcsin(mu_cmb) - target / (A * ck)
        if 0 < arg < np.pi / 2:
            md = np.sin(arg)
            zd = b_dsic(MU0) / b_dsic(md) - 1.0
            star = "  <- естественная (A=1)" if abs(A - 1.0) < 1e-9 else ""
            print(f"  {A:>6.1f} {md:>10.6f} {zd:>10.3f}{star}")
        else:
            print(f"  {A:>6.1f} {'нет реш.':>10}")
    print("\n  Степеней свободы = 0: одно наблюдение (d_LSS) фиксирует один")
    print("  параметр (μ_d при A=1). Это КАЛИБРОВКА, не предсказание. Плагин")
    print("  это и заявляет в A.7: «μ_d не легален, второго теста нет».\n")


# ==============================================================================
#  [5] ГДЕ ИСКАТЬ ВТОРОЙ ТЕСТ: СЛЕД μ_d НА ВЫСОКОМ z
# ==============================================================================
def step_where_to_test(mu_d, z_det):
    print("=" * 70)
    print("[5] Где μ_d оставляет проверяемый след (поправка D_M выше порога)")
    print("=" * 70)
    print(f"  {'z':>6} {'объект':<18} {'поправка к D_M':>16}")
    print("  " + "-" * 44)
    for z, obj in [(z_det, "порог отлипания"), (5.0, "галактики JWST"),
                   (6.0, "квазары"), (8.0, "реионизация"),
                   (11.0, "первые галактики"), (PLANCK["z_star"], "CMB (калибровка)")]:
        me = mu_e_of_z(z)
        base = DM_base(me)
        corr = DM_corrected(me, mu_d)
        print(f"  {z:>6.2f} {obj:<18} {100*(corr/base-1):>+13.1f} %")
    print()
    print("  Досягаемость зондов:")
    print("    DESI DR2 (есть)      z ≤ 2.33   — ниже порога")
    print("    DESI DR3 (~2027)     z ≤ 3.55   — ниже порога")
    print("    DESI-II/LBG (~2029)  2.5<z<3.5  — ниже порога")
    print()
    print("  ВЫВОД. Радиальный BAO в окне 2.33–4.53 расходится с ΛCDM (до ~4 %")
    print("  на z≈3.5), но это след ЯДРА (D_H=c/H зависит только от μ₀), а не")
    print("  μ_d. Сам порог влияет лишь на D_M выше z≈4.5, где точных линеек,")
    print("  кроме CMB-калибровки, нет. Второго НЕЗАВИСИМОГО якоря для μ_d в")
    print("  досягаемых данных не существует — плагин остаётся некалибруемым")
    print("  извне до появления точной шкалы расстояний на z ≳ 5–6.\n")


# ==============================================================================
#  ГЛАВНЫЙ ПОТОК
# ==============================================================================
def main():
    self_check_planck()                              # [0]
    mu_cmb, DM0, deficit = step_core_geometry()      # [1]
    mu_d, z_det = step_fix_threshold(mu_cmb, DM0)    # [2]
    step_late_universe_intact(mu_d, z_det)           # [3]
    step_degeneracy(mu_cmb, DM0)                      # [4]
    step_where_to_test(mu_d, z_det)                  # [5]
    print("=" * 70)
    print("Готово. Арифметика плагина воспроизведена; опорная цифра Planck")
    print(f"сверена; поздняя Вселенная (z ≤ {z_det:.2f}) не затронута. μ_d остаётся")
    print("калибровкой по одному наблюдению (0 ст. свободы) — это заявлено честно.")
    print("=" * 70)


if __name__ == "__main__":
    main()
