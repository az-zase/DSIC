"""
Om(z)-диагностика из данных: прямое сравнение DSIC и ΛCDM.

    Om(z) = (E² − 1) / ((1+z)³ − 1),   E(z) = H(z)/H₀

Для плоской ΛCDM Om(z) = Ωm = const (горизонтальная линия).
Для DSIC Om(z) имеет U-образный вид (минимум около z=2, подъём к высоким z).
Реконструкция Om(z) выполняется непосредственно из данных и сопоставляется
с предсказаниями обеих моделей.

Источник H(z): космические хронометры (метод дифференциального возраста
галактик) — прямые модельно-независимые измерения, не требующие r_d или
предположений о геометрии. Дополнительно Om(z) строится из BAO D_H/r_d
как второй независимый источник.

Зависимости: numpy, scipy, matplotlib.
"""

import numpy as np
import matplotlib.pyplot as plt
from scipy.optimize import brentq

c = 299792.458

# ---------------------------------------------------------------------
# 1. Космические хронометры H(z) (компиляция Moresco et al.)
#    Столбцы: z, H(z) [км/с/Мпк], σH. Значения следует сверять
#    с актуальной компиляцией; структура теста от этого не зависит.
# ---------------------------------------------------------------------
CC = np.array([
 [0.07, 69.0, 19.6],[0.09, 69.0, 12.0],[0.12, 68.6, 26.2],[0.17, 83.0, 8.0],
 [0.179,75.0, 4.0],[0.199,75.0, 5.0],[0.20, 72.9,29.6],[0.27, 77.0,14.0],
 [0.28, 88.8,36.6],[0.352,83.0,14.0],[0.38, 83.0,13.5],[0.40, 95.0,17.0],
 [0.4004,77.0,10.2],[0.425,87.1,11.2],[0.445,92.8,12.9],[0.47, 89.0,49.6],
 [0.4783,80.9,9.0],[0.48, 97.0,62.0],[0.593,104.0,13.0],[0.68,92.0,8.0],
 [0.75,98.8,33.6],[0.781,105.0,12.0],[0.80,113.1,28.5],[0.875,125.0,17.0],
 [0.88,90.0,40.0],[0.90,117.0,23.0],[1.037,154.0,20.0],[1.30,168.0,17.0],
 [1.363,160.0,33.6],[1.43,177.0,18.0],[1.53,140.0,14.0],[1.75,202.0,40.0],
 [1.965,186.5,50.4],
])
z_cc, H_cc, sH_cc = CC[:,0], CC[:,1], CC[:,2]

# ---------------------------------------------------------------------
# 2. BAO DESI DR2: D_H/r_d → H(z) = c/D_H = c/((D_H/rd)·rd)
#    Столбцы: z, D_H/rd, σ.
# ---------------------------------------------------------------------
BAO_DH = np.array([
 [0.510,21.863,0.425],[0.706,19.455,0.330],[0.930,17.641,0.193],
 [1.317,14.176,0.221],[1.491,12.817,0.516],[2.330, 8.632,0.101],
])

# ---------------------------------------------------------------------
# 3. Модельные кривые Om(z) для DSIC и ΛCDM
#    (параметры из совместного фита; при необходимости заменить
#    значениями из соответствующего прогона).
# ---------------------------------------------------------------------
mu0_fit, Om_fit = 0.7615, 0.3044
def b(m): return np.sqrt(1-m*m)/(m*m)
def mu_e(zv,mu0): return brentq(lambda m:b(m)-b(mu0)/(1+zv),1e-12,1-1e-15)
def E_DSIC(zv,mu0):
    me=mu_e(zv,mu0); return ((2-me**2)/(1-me**2))/((2-mu0**2)/(1-mu0**2))
def E_LCDM(zv,Om): return np.sqrt(Om*(1+zv)**3+(1-Om))
def Om_of_E(zv,E): return (E**2-1)/((1+zv)**3-1)

zgrid=np.linspace(0.05,2.5,200)
Om_curve_D=[Om_of_E(z,E_DSIC(z,mu0_fit)) for z in zgrid]
Om_curve_L=[Om_of_E(z,E_LCDM(z,Om_fit)) for z in zgrid]

# ---------------------------------------------------------------------
# 4. Реконструкция Om(z) из данных.
#    Требуется нормировка H0. Здесь H0 оценивается из самих хронометров
#    как параметр совместной подгонки H(z); анализируется форма Om(z).
#    Устойчивость вывода к выбору H0 проверяется в отдельном скрипте.
# ---------------------------------------------------------------------
from scipy.optimize import minimize
def chi2_H0Om(p):
    H0,Om=p
    if not(50<H0<90 and 0.05<Om<0.6): return 1e12
    pred=H0*np.sqrt(Om*(1+z_cc)**3+(1-Om))
    return np.sum(((pred-H_cc)/sH_cc)**2)
r=minimize(chi2_H0Om,[70,0.3],method="Nelder-Mead")
H0_cc,Om_cc=r.x
print(f"H0 из хронометров (нормировка Om(z)): {H0_cc:.1f}  (Om≈{Om_cc:.3f})")

# Om(z) из точек хронометров
E_data=H_cc/H0_cc
Om_data=Om_of_E(z_cc,E_data)
# Ошибки Om(z) из ошибок H(z) (через производную)
dOm=np.abs(2*E_data/((1+z_cc)**3-1))*(sH_cc/H0_cc)

# Om(z) из BAO (требуется r_d; стандартное значение 147.1 Мпк для нормировки H)
rd=147.1
z_b=BAO_DH[:,0]; DHrd=BAO_DH[:,1]; sDHrd=BAO_DH[:,2]
H_bao=c/(DHrd*rd)
sH_bao=H_bao*(sDHrd/DHrd)
E_bao=H_bao/H0_cc
Om_bao=Om_of_E(z_b,E_bao)
dOm_bao=np.abs(2*E_bao/((1+z_b)**3-1))*(sH_bao/H0_cc)

# ---------------------------------------------------------------------
# 5. График
# ---------------------------------------------------------------------
plt.figure(figsize=(10,6))
plt.plot(zgrid,Om_curve_L,'-',color='steelblue',lw=2,label=f'ΛCDM (плоская, Ωm={Om_fit:.3f})')
plt.plot(zgrid,Om_curve_D,'-',color='crimson',lw=2,label=f'DSIC (U-образная, μ₀={mu0_fit:.3f})')
plt.errorbar(z_cc,Om_data,yerr=dOm,fmt='o',color='black',alpha=0.55,
             capsize=2,label='данные: хронометры H(z)')
plt.errorbar(z_b,Om_bao,yerr=dOm_bao,fmt='s',color='darkgreen',ms=8,
             capsize=3,label='данные: BAO D_H/r_d')
plt.axhline(Om_fit,color='steelblue',ls=':',alpha=0.5)
plt.xlabel('z'); plt.ylabel('Om(z)')
plt.title('Om(z)-диагностика из данных: плоская (ΛCDM) или U-образная (DSIC)')
plt.legend(); plt.ylim(-0.1,0.9); plt.grid(alpha=0.3)
plt.tight_layout(); plt.savefig('Om_z_reconstruction.png',dpi=130); plt.show()

# ---------------------------------------------------------------------
# 6. Количественное сравнение форм с данными
# ---------------------------------------------------------------------
def chi2_curve(Efunc):
    c1=np.sum(((Om_data-np.array([Om_of_E(z,Efunc(z)) for z in z_cc]))/dOm)**2)
    c2=np.sum(((Om_bao-np.array([Om_of_E(z,Efunc(z)) for z in z_b]))/dOm_bao)**2)
    return c1+c2
chi_D=chi2_curve(lambda z:E_DSIC(z,mu0_fit))
chi_L=chi2_curve(lambda z:E_LCDM(z,Om_fit))
ndata=len(z_cc)+len(z_b)
print(f"\nOm(z): данные vs кривые ({ndata} точек):")
print(f"  χ²(DSIC, U-образная) = {chi_D:.1f}")
print(f"  χ²(ΛCDM, плоская)    = {chi_L:.1f}")
verdict=('данные ближе к ΛCDM' if chi_D>chi_L+2
         else 'данные ближе к DSIC' if chi_L>chi_D+2
         else 'различие незначимо (уровень шума данных)')
print(f"  Δχ² = {chi_D-chi_L:+.1f}   ({verdict})")
print("\nГрафик сохранён: Om_z_reconstruction.png")
